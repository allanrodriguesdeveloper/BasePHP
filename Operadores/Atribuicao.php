/**
 *  Um operador de atribuição é utilizado para definir uma variavel
 * atribuindo-lhe um valor. O operador básico de atribuição é =
 */

$var = 100;
$var += 40; // Soma
$var -= 10; // Subtração
$var /= 10; // Multiplicação
$var *= 10; // Divisão

echo $var;



$a = 10;
++$a; // Pré-Incremento $a em um e, então retorna $a
$a++; // Pós-Incremento retorna $a e, então, incrementa $a em um
--$a; // Pré-Decremento Decrementa $a em um e, então retorna $a
$a--; // Pós-Decremento Retorna $a e, então, decrementa $a em um

echo $a;
