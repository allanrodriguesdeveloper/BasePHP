################################################


/**
 * For é um estrutura de controle que estabelece um laçõ de repetição
 * baseada em um contador é muito similar ao comando while(). O for 
 * é controlado por um de três comandos que estabelecem contagem, ou
 * seja, o bloco de comandos será executado um certo numero de vezes
 */


for ($a = 2; $a <= 10; $a++) {
    echo $a . '<br>';
}


echo '<br>';


for ($a = 2; $a <= 20; $a += 2) {
    echo $a . '<br>';
}
